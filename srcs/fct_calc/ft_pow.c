#include "../../includes/libft.h"

int		ft_pow(int nb, int pow)
{
	if (nb == 0 || pow < 0)
		return (0);
	if (pow == 0)
		return (1);
	nb *= ft_pow(nb, pow - 1);
	return (nb);
}
